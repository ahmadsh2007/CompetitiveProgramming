void solve() {
    int n; cin >> n;

    vector<pair<string, int>> rounds(n);
    map<string, int> score;

    for (int i = 0; i < n; ++i) {
        cin >> rounds[i].first >> rounds[i].second;
        score[rounds[i].first] += rounds[i].second;
    }

    int mx = LLONG_MIN;
    for (auto &[name, finalScore] : score) {
        mx = max(mx, finalScore);
    }

    map<string, int> curScore;
    for (int i = 0; i < n; ++i) {
        string name = rounds[i].first;
        int curPlayerScore = rounds[i].second;

        curScore[name] += curPlayerScore;

        if (score[name] == mx && curScore[name] >= mx) {
            cout << name << endl;
            return;
        }
    }
}

