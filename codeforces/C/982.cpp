vector<vector<int>> adj;
int ans = 0;

int dfs(int u, int p) {
    int curSubTreeSize = 1;

    for (auto &v : adj[u]) {
        if (v != p) {
            int size = dfs(v, u);

            if (size % 2 == 0) ans++;
            else curSubTreeSize += size;
        }
    }

    return curSubTreeSize;
}

void solve() {
    int n; cin >> n;
    
    adj.resize(n + 1, {});
    for (int i = 1; i < n; ++i) {
        int u, v; cin >> u >> v;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }

    if (n % 2) { cout << "-1\n"; return; }

    ans = 0;
    dfs(1, 0);

    cout << ans << endl;
}

