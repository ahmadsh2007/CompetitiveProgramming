int ret(string n, string s, int fourCnt, int sevenCnt, int len) {
    if ((int) s.size() == len) {
        if (fourCnt == sevenCnt)
            if ((int) s.size() > (int) n.size() || stoll(s) >= stoll(n))
                return stoll(s);
        return LLONG_MAX;
    }

    int res = LLONG_MAX;
    res = min(res, ret(n, s + "4", fourCnt + 1, sevenCnt, len));
    res = min(res, ret(n, s + "7", fourCnt, sevenCnt + 1, len));

    return res;
}

void solve() {
    str n; cin >> n;

    int len = (int) n.size();
    if (len % 2) len++;

    int ans = LLONG_MAX;
    
    while (ans == LLONG_MAX) {
        ans = ret(n, "", 0, 0, len);
        len += 2;
    }

    cout << ans << endl;
}

